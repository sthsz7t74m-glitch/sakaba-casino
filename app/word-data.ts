// 表記と読みが同じ語は一件にまとめ、全ゲームで同じ語彙を参照する。
export const answerWords: AnswerWord[] = Array.from(
  new Map([...curatedAnswerWords, ...extraAnswerWords].map((entry) => [`${entry.surface}\u0000${entry.reading}`, entry])).values(),
);

export function findAnswerWords(kana: string, length: number): AnswerWord[] {
  return answerWords
    .filter((entry) => entry.reading.startsWith(kana) && Array.from(entry.reading).length === length)
    .sort((a, b) => Number(Boolean(a.proper)) - Number(Boolean(b.proper)))
    .slice(0, 5);
}
